package controller;

import context.CustomerDBContext;
import controller.auth.BaseRequiredAuthController;
import java.io.IOException;
import java.io.PrintWriter;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import model.Customer;

/**
 *
 * @author 84984
 */
public class UpdateCustomerController extends  BaseRequiredAuthController {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void processGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("id");
        CustomerDBContext cusDB = new  CustomerDBContext();
        Customer oldCustomer  = cusDB.getCustomer(id);
        request.setAttribute("oldCustomer", oldCustomer);
        request.getRequestDispatcher("UpdateCustomer.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void processPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("id");
        CustomerDBContext cusDB = new  CustomerDBContext();
        String name=request.getParameter("name");
        String phoneNumber=request.getParameter("phoneNumber");
        int Total= Integer.parseInt(request.getParameter("total"));
        int Payed= Integer.parseInt(request.getParameter("payed"));
        int Owes= Integer.parseInt(request.getParameter("owes"));
        cusDB.update(id, name, phoneNumber, Total, Payed, Owes);
        response.sendRedirect("list");
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
